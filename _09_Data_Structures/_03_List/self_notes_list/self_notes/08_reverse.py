"""
 8. reverse
      list.reverse()    Reverses objects of list in place
"""

list1 = [1, 2, 3, 4, 5]

print("Given list1 is : ", list1)

list1.reverse()

print("List after reverse : ", list1)

list2 = [23, 76, 23, 32, 53, 34, 25, 21, 53, 43, 16, 25, 7, 53, 13, 59]

print("Given list2 is : ", list2)

list2.reverse()

print("List after reverse : ", list2)
